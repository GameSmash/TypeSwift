import Foundation

enum SwiftNumber: String {
    case float = "Float"
    case int = "Int"
    case uint = "UInt"
    case double = "Double"
    
    case float32 = "Float32"
    case int32 = "Int32"
    case uint32 = "UInt32"
    
    case float64 = "Float64"
    case int64 = "Int64"
    case uint64 = "UInt64"
}
